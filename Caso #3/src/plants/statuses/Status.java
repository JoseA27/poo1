package plants.statuses;

public class Status {
	private String name;
	private String StatusPictureURL;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStatusPictureURL() {
		return StatusPictureURL;
	}
	public void setStatusPictureURL(String statusPictureURL) {
		StatusPictureURL = statusPictureURL;
	}
}